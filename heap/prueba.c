int main(){
	MaxHeap h;
	int arr[20] = {1,2,3,4,5,6,7,8,9,25,4,7,8,5,6,8,35,14,78,2};

	maxHeapInit(&h);
	insert(&h, 170);
	insert(&h, 120);
	insert(&h, 190);
	insert(&h, 10);
	insert(&h, 70);
	insert(&h, 180);
	insert(&h, 150);
	insert(&h, 50);
	insert(&h, 65);

	printHeap(&h);

	printf("\nMax: %d\n", getMax(&h));
	printf("\nSize (number of nodes): %d\n", getSize(&h));
	printf("\nEmpty? %d\n", isEmpty(&h));
	printf("\nExtractMax: %d\n", extractMax(&h));
	printf("\nMax: %d\n", getMax(&h));

	printHeap(&h);

	printf("\nRemoving 120 from heap...\n");
	remove(&h, 1);
	printHeap(&h);

	printf("\nRemoving 150 from heap...\n");
	remove(&h, 7);
	printHeap(&h);

	heapsort(arr, 20);

	printf("\n");
	for (int i = 0; i < 20; i++){
		printf("arr[%d]: %d\n", i, arr[i]);
	}

	printf("\n\nHeap at the end of program:\n");
	printHeap(&h);
	printf("END\n");

	return 0;
}
