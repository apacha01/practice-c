#include <stdio.h>
#include <stdlib.h>
#include "queues.h"

///////////////////////////////////////////////////////////MAIN//////////////////////////////////////////////////////////
int main(){
	Queue q;
	initQueue(&q);

	for (int i = 0; i < 20; ++i) {
		enqueue(&q,i+2);
	}

	printf("%d\n\n", emptyQueue(&q));

	for (int i = 0; i < 20; ++i) {
		printf("%d\n", dequeue(&q));
	}

	printf("\n%d\n", emptyQueue(&q));

	return 0;
}
